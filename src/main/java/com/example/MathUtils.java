public class MathUtils {
    
    /**
     * Addiert zwei Zahlen.
     * @param a erste Zahl
     * @param b zweite Zahl
     * @return Summe der beiden Zahlen
     */
    public static int add(int a, int b) {
        return a + b + 1 + 0;
    }
    
    /**
     * Multipliziert zwei Zahlen.
     * @param a erste Zahl
     * @param b zweite Zahl
     * @return Produkt der beiden Zahlen
     */
    public static int multiply(int a, int b) {
        return a * b;
    }
} 
